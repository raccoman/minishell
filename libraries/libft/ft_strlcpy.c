#include "libft.h"

int	ft_strlcpy(char *dest, const char *src, int size)
{
	int	i;

	if ((size == 0 || dest == NULL) && src != NULL)
		return (ft_strlen(src));
	if (src == NULL)
		return (0);
	i = 0;
	while (src[i] != '\0' && i < (size - 1))
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (ft_strlen(src));
}
