int	ft_is_num(const char *str)
{
	short	correct;

	correct = 0;
	if (*str == '-')
		str++;
	while (*str)
	{
		if (!('0' <= *str && *str <= '9'))
			return (0);
		correct = 1;
		str++;
	}
	return (correct);
}
